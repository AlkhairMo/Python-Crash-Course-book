age = 19
if age < 2:
    print('this person is baby')
elif 2 <= age < 4:
    print('this person is toddler')
elif 4 <= age < 13:
    print('this person is kid')
elif 13 <= age < 20:
    print('this person is teenager')
elif 20 <= age < 65:
    print('this person is adult')
elif age >= 65:
    print('this person is an elder')
